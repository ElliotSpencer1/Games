function startzoomer(){
  if(!startzoom){
    if(znotset){
      z = 0.5
      znotset = false;
    }
    if(z >= 0.5 && z < 1){
      camera.zoomTo(1, 0.00001);
      z = 1;
    }
    if(z >= 1 && z < 2){
      camera.zoomTo(2, 0.0001);
      z = 2;
    }
    if(z >= 2 && z <= 4){
      camera.zoomTo(4, 0.1);
      z = 4;
    }
    if(camera.zoom == 4){
      z = 4;
      startzoom = true;
    }
  }
}

function cameramovement(){
  camera.y = player.y;
  camera.x = player.x;
  if(startzoom){
    camera.zoom = z;
  }
}

function zoomcontrol(){
  if((kb.pressing("ARROW_UP")) && z <= 30 && !cameralocked){
    z = z + 0.1;
    console.log(z);
  }
  if((kb.pressing("ARROW_DOWN")) && z >= 1.5 && !cameralocked){
    z = z - 0.1;
    console.log(z);
  }
}

function movement(){
  if(kb.pressing("a")){
    player.x -= base;
  }
  if(kb.pressing("d")){
    player.x += base;
  }
  if(kb.pressing("w")){
    player.y -= base;
  }
  if(kb.pressing("s")){
    player.y += base;
  }
  if(kb.pressing("shift") && !disableshift){
    base = 4;
  }
  if(!(kb.pressing("shift")) || disableshift){
    base = 2;
  }
  if(player.colliding(walls)){
    disableshift = true;
  }
  if(!player.colliding(walls)){
    disableshift = false;
  }
}

